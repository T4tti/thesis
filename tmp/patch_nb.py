CELL19 = [
    "# ============================================================\n",
    "# CORN Loss + Hybrid (CORN + FuzzyFocal)\n",
    "# ============================================================\n",
    "\n",
    "class CORNLoss(nn.Module):\n",
    "    \"\"\"\n",
    "    CORN: Conditional Ordinal Regression Network Loss.\n",
    "\n",
    "    For K classes uses K-1 binary classifiers where classifier k\n",
    "    predicts P(y > k | y >= k, x) — conditional probability.\n",
    "    This ensures rating predictions respect the ordinal structure\n",
    "    (D < CCC < B < BB < BBB < A < AA < AAA).\n",
    "\n",
    "    Reference: Shi et al. \"Deep Neural Networks for Rank Consistent\n",
    "    Ordinal Regression based on Conditional Probabilities\" (2022)\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self, num_classes: int):\n",
    "        super().__init__()\n",
    "        self.num_classes = num_classes\n",
    "        self.K = num_classes - 1\n",
    "\n",
    "    def forward(self, logits: torch.Tensor, targets: torch.Tensor) -> torch.Tensor:\n",
    "        \"\"\"\n",
    "        Args:\n",
    "            logits:  (B, K-1) binary logits from CORN head\n",
    "            targets: (B,) ordinal labels in {0, …, K-1}\n",
    "        Returns:\n",
    "            Scalar loss (mean across K-1 conditional classifiers)\n",
    "        \"\"\"\n",
    "        total_loss = torch.tensor(0.0, device=logits.device, requires_grad=False)\n",
    "        n_active   = 0\n",
    "\n",
    "        for k in range(self.K):\n",
    "            # Only samples with y >= k are used for classifier k\n",
    "            mask = targets >= k\n",
    "            if mask.sum() == 0:\n",
    "                continue\n",
    "            binary_label = (targets[mask] > k).float()\n",
    "            loss_k = F.binary_cross_entropy_with_logits(\n",
    "                logits[mask, k], binary_label, reduction='mean'\n",
    "            )\n",
    "            total_loss = total_loss + loss_k\n",
    "            n_active  += 1\n",
    "\n",
    "        return total_loss / max(n_active, 1)\n",
    "\n",
    "\n",
    "class FuzzyFocalLoss(nn.Module):\n",
    "    \"\"\"Focal Loss weighted by fuzzy membership (unchanged from V1).\"\"\"\n",
    "\n",
    "    def __init__(self, gamma=1.5, class_weight=None, reduction='mean'):\n",
    "        super().__init__()\n",
    "        self.gamma        = gamma\n",
    "        self.class_weight = class_weight\n",
    "        self.reduction    = reduction\n",
    "\n",
    "    def forward(self, inputs, targets, fuzzy_weights=None):\n",
    "        ce_loss = F.cross_entropy(inputs, targets, reduction='none')\n",
    "        pt      = torch.exp(-ce_loss)\n",
    "        focal   = ((1 - pt) ** self.gamma) * ce_loss\n",
    "\n",
    "        if self.class_weight is not None:\n",
    "            focal = focal * self.class_weight[targets]\n",
    "        if fuzzy_weights is not None:\n",
    "            focal = focal * fuzzy_weights\n",
    "\n",
    "        if self.reduction == 'mean':\n",
    "            return focal.mean()\n",
    "        elif self.reduction == 'sum':\n",
    "            return focal.sum()\n",
    "        return focal\n",
    "\n",
    "\n",
    "class HybridCORNFuzzyLoss(nn.Module):\n",
    "    \"\"\"\n",
    "    Hybrid Loss:  L = alpha * L_CORN + (1-alpha) * L_FuzzyFocal\n",
    "\n",
    "    - CORN captures ordinal rating structure (penalises distant errors more)\n",
    "    - FuzzyFocal handles class imbalance + boundary sample uncertainty\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self, num_classes: int, alpha: float = 0.7,\n",
    "                 gamma: float = 1.5, class_weights=None):\n",
    "        super().__init__()\n",
    "        self.alpha      = alpha\n",
    "        self.corn_loss  = CORNLoss(num_classes)\n",
    "        self.focal_loss = FuzzyFocalLoss(gamma=gamma, class_weight=class_weights)\n",
    "\n",
    "    def forward(self, logits_corn, logits_ce, targets, fuzzy_weights=None):\n",
    "        \"\"\"\n",
    "        logits_corn: (B, K-1)  CORN binary outputs\n",
    "        logits_ce:   (B, K)    CE / FuzzyFocal outputs\n",
    "        targets:     (B,)      ordinal labels\n",
    "        \"\"\"\n",
    "        l_corn  = self.corn_loss(logits_corn, targets)\n",
    "        l_focal = self.focal_loss(logits_ce, targets, fuzzy_weights=fuzzy_weights)\n",
    "        return self.alpha * l_corn + (1.0 - self.alpha) * l_focal\n",
    "\n",
    "\n",
    "# ── Utility functions for CORN predictions ──────────────────────────────\n",
    "\n",
    "def corn_predict(logits_corn: torch.Tensor) -> torch.Tensor:\n",
    "    \"\"\"Convert CORN binary logits → class predictions.\"\"\"\n",
    "    return (torch.sigmoid(logits_corn) > 0.5).sum(dim=1).long()\n",
    "\n",
    "\n",
    "def corn_to_probs(logits_corn: torch.Tensor, num_classes: int) -> torch.Tensor:\n",
    "    \"\"\"\n",
    "    Convert CORN logits → ordinal class probability distribution.\n",
    "    P(y=k) derived from cumulative conditional probabilities.\n",
    "    \"\"\"\n",
    "    probs_gt = torch.sigmoid(logits_corn)         # (B, K-1): P(y > k)\n",
    "    p0    = 1.0  - probs_gt[:, 0:1]              # P(y = 0)\n",
    "    pmid  = probs_gt[:, :-1] - probs_gt[:, 1:]   # P(y = k), 0 < k < K-1\n",
    "    plast = probs_gt[:, -1:]                      # P(y = K-1)\n",
    "    probs = torch.cat([p0, pmid, plast], dim=1)   # (B, K)\n",
    "    probs = probs.clamp(min=1e-8)\n",
    "    probs = probs / probs.sum(dim=1, keepdim=True)\n",
    "    return probs\n",
    "\n",
    "\n",
    "# ── Adjacent accuracy helper ─────────────────────────────────────────────\n",
    "\n",
    "def adjacent_accuracy(y_true: np.ndarray, y_pred: np.ndarray) -> float:\n",
    "    \"\"\"Fraction of predictions within ±1 ordinal step of true label.\"\"\"\n",
    "    return float(np.mean(np.abs(y_pred.astype(int) - y_true.astype(int)) <= 1))\n",
    "\n",
    "\n",
    "# ── Setup Loss & Class Weights ───────────────────────────────────────────\n",
    "\n",
    "def compute_fuzzy_sample_weights(X, y, n_classes):\n",
    "    \"\"\"Per-sample fuzzy weights based on Gaussian distance to class centroid.\"\"\"\n",
    "    centroids = np.zeros((n_classes, X.shape[1]))\n",
    "    for c in range(n_classes):\n",
    "        mask = y == c\n",
    "        centroids[c] = X[mask].mean(axis=0) if mask.sum() > 0 else X.mean(axis=0)\n",
    "\n",
    "    weights = np.ones(len(X))\n",
    "    sigma   = 2.0\n",
    "    for i in range(len(X)):\n",
    "        dist       = np.linalg.norm(X[i] - centroids[y[i]])\n",
    "        weights[i] = np.exp(-0.5 * (dist / sigma) ** 2)\n",
    "\n",
    "    mn, mx    = weights.min(), weights.max()\n",
    "    weights   = 0.3 + 0.7 * (weights - mn) / (mx - mn + 1e-8)\n",
    "    return weights\n",
    "\n",
    "\n",
    "fuzzy_weights_train = compute_fuzzy_sample_weights(\n",
    "    X_all[train_idx], y_all[train_idx], n_classes\n",
    ")\n",
    "print(f\"Fuzzy weights — min:{fuzzy_weights_train.min():.4f}  \"\n",
    "      f\"max:{fuzzy_weights_train.max():.4f}  mean:{fuzzy_weights_train.mean():.4f}\")\n",
    "\n",
    "# Class weights for imbalance\n",
    "all_train_labels  = y_all[train_idx]\n",
    "class_counts      = np.bincount(all_train_labels, minlength=n_classes).astype(float)\n",
    "class_counts      = np.maximum(class_counts, 1.0)\n",
    "raw_w             = 1.0 / class_counts\n",
    "cap               = np.percentile(raw_w, 95)\n",
    "class_weights     = np.clip(raw_w, None, cap)\n",
    "class_weights     = class_weights / class_weights.mean()\n",
    "class_weights_tensor = torch.FloatTensor(class_weights).to(device)\n",
    "\n",
    "FOCAL_GAMMA = 1.5\n",
    "CORN_ALPHA  = 0.7   # weight for CORN vs FuzzyFocal\n",
    "criterion = HybridCORNFuzzyLoss(\n",
    "    num_classes   = n_classes,\n",
    "    alpha         = CORN_ALPHA,\n",
    "    gamma         = FOCAL_GAMMA,\n",
    "    class_weights = class_weights_tensor,\n",
    ")\n",
    "print(f\"HybridCORNFuzzyLoss: {CORN_ALPHA:.0%} CORN + {1-CORN_ALPHA:.0%} FuzzyFocal\")\n",
    "print(f\"Class weight range:  [{class_weights.min():.4f}, {class_weights.max():.4f}]\")\n",
]
