    "            nn.Linear(in_channels, hidden_channels),\n",
    "            nn.LayerNorm(hidden_channels),\n",
    "            nn.GELU(),\n",
    "        )\n",
    "\n",
    "        # === Feature Graph Module (Level 1 — upgraded) ===\n",
    "        self.feat_gat_layers = nn.ModuleList()\n",
    "        self.feat_bns        = nn.ModuleList()\n",
    "\n",
    "        for _ in range(num_gat_layers):\n",
    "            self.feat_gat_layers.append(\n",
    "                GATConv(\n",
    "                    hidden_channels,\n",
    "                    hidden_channels // heads,\n",
    "                    heads=heads,\n",
    "                    concat=True,\n",
    "                    edge_dim=1,\n",
    "                    dropout=0.1,\n",
    "                )\n",
    "            )\n",
    "            self.feat_bns.append(nn.BatchNorm1d(hidden_channels))\n",
    "\n",
    "        pool_dim = hidden_channels * 2  # mean + max concat\n",
    "\n",
    "        # === CORN Head (K-1 binary classifiers) ===\n",
    "        self.corn_head = nn.Sequential(\n",
    "            nn.Linear(pool_dim, hidden_channels),\n",
    "            nn.LayerNorm(hidden_channels),\n",
    "            nn.GELU(),\n",
    "            nn.Dropout(fc_dropout * 0.5),\n",
    "            nn.Linear(hidden_channels, n_classes - 1),   # K-1 outputs\n",
    "        )\n",
    "\n",
    "        # === CE Head (K-class for FuzzyFocal auxiliary loss) ===\n",
    "        self.ce_head = nn.Sequential(\n",
    "            nn.Linear(pool_dim, hidden_channels * 2),\n",
    "            nn.BatchNorm1d(hidden_channels * 2),\n",
    "            nn.GELU(),\n",
    "            nn.Dropout(fc_dropout),\n",
    "            nn.Linear(hidden_channels * 2, hidden_channels),\n",
    "            nn.GELU(),\n",
    "            nn.Dropout(fc_dropout * 0.5),\n",
    "            nn.Linear(hidden_channels, n_classes),        # K outputs\n",
    "        )\n",
    "\n",
    "    def forward(self, x, edge_index, edge_attr, batch):\n",
    "        \"\"\"\n",
    "        Returns:\n",
    "            logits_corn: (B, K-1)  CORN binary logits\n",
    "            logits_ce:   (B, K)    CE logits\n",
    "        \"\"\"\n",
    "        # Input projection\n",
    "        h = self.input_proj(x)  # (N_total, hidden)\n",
    "\n",
    "        # GAT layers with residual connections\n",
    "        for gat, bn in zip(self.feat_gat_layers, self.feat_bns):\n",
    "            h_new  = gat(h, edge_index, edge_attr=edge_attr)\n",
    "            h_new  = bn(h_new)\n",
    "            h_new  = F.gelu(h_new)\n",
    "            h_new  = F.dropout(h_new, p=self.dropout, training=self.training)\n",
    "            h      = h + h_new   # residual (dims always match after input_proj)\n",
    "\n",
    "        # Dual pooling\n",
    "        h_mean    = global_mean_pool(h, batch)\n",
    "        h_max     = global_max_pool(h, batch)\n",
    "        graph_emb = torch.cat([h_mean, h_max], dim=1)  # (B, hidden*2)\n",
    "\n",
    "        return self.corn_head(graph_emb), self.ce_head(graph_emb)\n",
    "\n",
    "\n",
    "# ── Initialise ──────────────────────────────────────────────────────────\n",
    "model = HHGNNFuzzyV2CreditRating(\n",
