    "def expected_calibration_error(\n",
    "    probabilities: Any,\n",
    "    targets: Any,\n",
    "    *,\n",
    "    n_bins: int = 15,\n",
    ") -> float:\n",
    "    \"\"\"Top-label expected calibration error.\"\"\"\n",
