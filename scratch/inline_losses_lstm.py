    "    y_true = _validate_targets(targets, len(probs), probs.shape[1])\n",
    "    predictions = probs.argmax(axis=1)\n",
    "    confidence = probs.max(axis=1)\n",
    "    correctness = predictions == y_true\n",
    "    edges = np.linspace(0.0, 1.0, int(n_bins) + 1)\n",
    "    ece = 0.0\n",
    "    for index in range(int(n_bins)):\n",
    "        lower, upper = edges[index], edges[index + 1]\n",
    "        mask = (\n",
    "            (confidence >= lower) & (confidence < upper)\n",
    "            if index < int(n_bins) - 1\n",
    "            else (confidence >= lower) & (confidence <= upper)\n",
    "        )\n",
    "        if mask.any():\n",
    "            ece += float(mask.mean()) * abs(\n",
    "                float(correctness[mask].mean()) - float(confidence[mask].mean())\n",
    "            )\n",
    "    return float(ece)\n",
