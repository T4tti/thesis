    "    else:\n",
    "        previous = np.asarray(last_y, dtype=np.int64).reshape(-1)\n",
    "        if len(previous) != len(y_true):\n",
    "            raise ValueError(\"last_y length does not match targets\")\n",
    "        change_mask = previous != y_true\n",
