def main() -> None:
    nb = json.loads(NOTEBOOK.read_text(encoding="utf-8"))

    # Find the cell by id or tag
    target_idx = None
    for idx, cell in enumerate(nb["cells"]):
        cell_id = cell.get("id", "")
        tags = cell.get("metadata", {}).get("tags", [])
        if cell_id == "two-tier-loss-contract" or "two-tier-loss-contract" in tags:
            target_idx = idx
            break

    if target_idx is None:
        raise RuntimeError("Cell 'two-tier-loss-contract' not found in notebook!")

    print(f"Found target cell at index {target_idx} (id={nb['cells'][target_idx].get('id')})")
    print(f"Old source lines: {len(nb['cells'][target_idx]['source'])}")

    # Replace source
    nb["cells"][target_idx]["source"] = NEW_SOURCE
    nb["cells"][target_idx]["outputs"] = []

    print(f"New source lines: {len(NEW_SOURCE)}")

    # Write back
    NOTEBOOK.write_text(
        json.dumps(nb, ensure_ascii=False, indent=1) + "\n",
        encoding="utf-8",
    )
    print("Done: updated", str(NOTEBOOK))


if __name__ == "__main__":
    main()
